    private void txtGalonesKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGalonesKeyPressed
        
        char inf = evt.getKeyChar();
        if ((inf >= '0' && inf <= '9') || (inf == '.' && !txtGalones.getText().contains(".")) ) {
            evt.consume();
        }
    }//GEN-LAST:event_txtGalonesKeyPressed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        Galones = 0;
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btnCancelActionPerformed

    private void btnAddGasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGasActionPerformed
        try {
            Galones = Double.parseDouble(txtGalones.getText());
            if (Galones > 0) {
                this.setVisible(false);
                this.dispose();
            }else {
                JOptionPane.showMessageDialog(null,"No has introducido Galones a tu compra","Compra o Efectuada",JOptionPane.WARNING_MESSAGE);
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Debe introducir un n√∫mero valido de Galones de Gasolina", "Error Dato", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnAddGasActionPerformed

